export type ConversationTranslationKey = keyof typeof enMessages;

export function conversationDefault(
  key: ConversationTranslationKey,
  values: Record<string, string | number> = {},
): string {
  return Object.entries(values).reduce(
    (text, [name, value]) => text.replaceAll(`{{${name}}}`, String(value)),
    conversationTranslations.en.messages[key] as string,
  );
}
