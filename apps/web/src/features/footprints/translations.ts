export type FootprintTranslationKey = keyof typeof enFootprints;

export function footprintDefault(
  key: FootprintTranslationKey,
  values: Record<string, string | number> = {},
): string {
  return Object.entries(values).reduce(
    (text, [name, value]) => text.replaceAll(`{{${name}}}`, String(value)),
    footprintTranslations.en.footprints[key] as string,
  );
}
